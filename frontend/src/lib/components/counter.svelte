<script lang="ts">
	import { page } from '$app/stores';
	import BlueButton from './blueButton.svelte';

	let count = 0;
	export let allowNegative: boolean = false;
	function add() {
		count += 1;
	}
	function sub() {
		if (!allowNegative && count == 0) {
			return;
		}
		count -= 1;
	}
</script>

<div class="m-3 p-3">
	<BlueButton text="-" onclick={sub} />
	<input
		class="m-3 rounded-full bg-slate-500 p-3 text-blue-100 text-center text-2xl "
		type="text"
		bind:value={count}
	/>
	<BlueButton text="+" onclick={add} />
</div>
